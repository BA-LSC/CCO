import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import emojis from "emojibase-data/en/data.json" with { type: "json" };
import messages from "emojibase-data/en/messages.json" with { type: "json" };

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, "../lib/emoji-picker-data.generated.ts");

const GROUP_LABELS = Object.fromEntries(
  messages.groups.map((group) => [group.order, titleCase(group.message)]),
);
GROUP_LABELS[9] = "Flags";

function titleCase(value) {
  return value
    .split(/\s*&\s*/)
    .map((part) => part.replace(/\b\w/g, (char) => char.toUpperCase()))
    .join(" & ");
}

function buildSearchText(entry) {
  return [entry.label, ...(entry.tags ?? [])].join(" ").toLowerCase();
}

function buildGroupKeywords(label, entries) {
  const words = new Set(
    label
      .toLowerCase()
      .split(/[^a-z0-9]+/)
      .filter((word) => word.length > 2),
  );

  for (const entry of entries.slice(0, 12)) {
    for (const tag of entry.tags ?? []) {
      if (tag.length > 2) words.add(tag);
    }
  }

  return [...words].sort();
}

const grouped = new Map();

for (const entry of emojis) {
  if (!entry.emoji || entry.group === 2) continue;

  const groupId = entry.group ?? 9;
  const list = grouped.get(groupId) ?? [];
  list.push(entry);
  grouped.set(groupId, list);
}

const groups = [...grouped.entries()]
  .sort(([left], [right]) => left - right)
  .map(([groupId, entries]) => {
    const sorted = [...entries].sort((left, right) => (left.order ?? 0) - (right.order ?? 0));
    const label = GROUP_LABELS[groupId] ?? `Group ${groupId}`;

    return {
      label,
      keywords: buildGroupKeywords(label, sorted),
      emojis: sorted.map((entry) => entry.emoji),
      searchTexts: sorted.map((entry) => buildSearchText(entry)),
    };
  });

const totalEmojis = groups.reduce((count, group) => count + group.emojis.length, 0);

const file = `// Generated by scripts/generate-emoji-picker-data.mjs. Do not edit by hand.

export type GeneratedEmojiPickerGroup = {
  label: string;
  keywords: readonly string[];
  emojis: readonly string[];
  searchTexts: readonly string[];
};

export const GENERATED_EMOJI_PICKER_GROUPS: readonly GeneratedEmojiPickerGroup[] = ${JSON.stringify(groups, null, 2)} as const;

export const GENERATED_EMOJI_PICKER_EMOJI_COUNT = ${totalEmojis} as const;
`;

writeFileSync(outPath, file, "utf8");
console.log(`Wrote ${totalEmojis} emojis in ${groups.length} groups to ${outPath}`);
